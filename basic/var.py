# Creating strings
string = "Hello, World!"

# Access characters
print(string[0])  # 'H'

# Slicing
print(string[7:12])  # 'World'

# Common operations
print(len(string))       # Length: 13
print(string.lower())    # 'hello, world!'
print(string.upper())    # 'HELLO, WORLD!'
print(string.replace("World", "Python"))  # 'Hello, Python!'